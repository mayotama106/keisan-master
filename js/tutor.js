/* tutor.js — ％計算に特化したルールベースの「先生ボット」（完全オフライン）
   自然文の％計算を解いたり、概念質問にやさしく答える。DOM 非依存の純粋ロジック。 */
(function (global) {
  'use strict';

  // 全角→半角・表記ゆれの正規化
  function normalize(s) {
    return String(s)
      .replace(/[０-９]/g, function (c) { return String.fromCharCode(c.charCodeAt(0) - 0xFEE0); })
      .replace(/，/g, ',').replace(/、/g, ',')
      .replace(/[％ﾊﾟ]/g, '%').replace(/パーセント|ぱーせんと/g, '%')
      .replace(/,/g, '')
      .replace(/\s+/g, ' ')
      .trim();
  }
  function fmt(n) {
    var r = Math.round(n * 100) / 100;
    return (Math.abs(r - Math.round(r)) < 1e-9) ? String(Math.round(r)) : String(r);
  }
  function has(s, words) { return words.some(function (w) { return s.indexOf(w) >= 0; }); }

  /* ---------- 計算問題を解く ---------- */
  function trySolve(s) {
    // 何％？： 「A は B の何%」
    var m = s.match(/(\d+(?:\.\d+)?)\D+(\d+(?:\.\d+)?)\D*(?:何|なん)\s*%/);
    if (m) {
      var a = +m[1], b = +m[2];
      if (b === 0) return { text: '0 で割ることはできないよ🙅 もとの数（くらべるもとの量）を 0 以外にしてね。' };
      var ans = a / b * 100;
      return {
        solved: true,
        text: ['📘 「' + a + ' は ' + b + ' の何％？」',
          'コツ：くらべる数 ÷ もとの数 × 100',
          '① ' + a + ' ÷ ' + b + ' = ' + fmt(a / b),
          '② ' + fmt(a / b) + ' × 100 = ' + fmt(ans) + '%',
          '👉 答えは ' + fmt(ans) + '％ だよ！'].join('\n')
      };
    }
    // ％引き（割引）
    m = s.match(/(\d+(?:\.\d+)?)\D+(\d+(?:\.\d+)?)\s*%\D*(?:引き|引|オフ|off|ひき|ｵﾌ)/);
    if (m) {
      var base = +m[1], p = +m[2], rest = 100 - p, ans2 = base * rest / 100;
      return {
        solved: true,
        text: ['📘 「' + base + ' の ' + p + '％引き」',
          'コツ：引いた残りは (100 − ' + p + ') = ' + rest + '％',
          '① ' + rest + '% = ' + (rest / 100),
          '② ' + base + ' × ' + (rest / 100) + ' = ' + fmt(ans2),
          '👉 答えは ' + fmt(ans2) + ' だよ！（' + base + ' から ' + fmt(base * p / 100) + ' 安くなる）'].join('\n')
      };
    }
    // ％増し
    m = s.match(/(\d+(?:\.\d+)?)\D+(\d+(?:\.\d+)?)\s*%\D*(?:増し|増|まし|マシ|アップ|up|ｱｯﾌﾟ)/);
    if (m) {
      var base3 = +m[1], p3 = +m[2], tot = 100 + p3, ans3 = base3 * tot / 100;
      return {
        solved: true,
        text: ['📘 「' + base3 + ' の ' + p3 + '％増し」',
          'コツ：増えた後は (100 + ' + p3 + ') = ' + tot + '％',
          '① ' + tot + '% = ' + (tot / 100),
          '② ' + base3 + ' × ' + (tot / 100) + ' = ' + fmt(ans3),
          '👉 答えは ' + fmt(ans3) + ' だよ！（' + fmt(base3 * p3 / 100) + ' 増える）'].join('\n')
      };
    }
    // 基本：「A の B%」
    m = s.match(/(\d+(?:\.\d+)?)\D+(\d+(?:\.\d+)?)\s*%/);
    if (m) {
      var base4 = +m[1], p4 = +m[2], ans4 = base4 * p4 / 100;
      return {
        solved: true,
        text: ['📘 「' + base4 + ' の ' + p4 + '％」',
          'コツ：％は「100ぶんのいくつ」。' + p4 + '% = ' + p4 + '/100 = ' + (p4 / 100),
          '① ' + p4 + '% を小数に → ' + (p4 / 100),
          '② ' + base4 + ' × ' + (p4 / 100) + ' = ' + fmt(ans4),
          '👉 答えは ' + fmt(ans4) + ' だよ！'].join('\n')
      };
    }
    return null;
  }

  /* ---------- 概念の説明 ---------- */
  var CONCEPTS = [
    {
      key: ['%とは', '%って', 'ぱーせんととは', 'パーセントとは', 'わりあい', '割合', '%の意味', 'ってなに', 'って何', '%がわからない', '%わからない'],
      text: ['❓ ％（パーセント）は「100 を全部としたときの、いくつ分か」を表すよ。',
        'たとえば 100円のうちの 30円 は 30％。半分は 50％、全部は 100％。',
        '🍕 ピザを100切れにして、そのうち何切れ？——それが％のイメージ！',
        '計算のときは ％ を 100 で割って小数にするのがコツだよ。例）25% → 0.25'].join('\n')
    },
    {
      key: ['割引', 'わりびき', 'オフ', '引き', '安く', 'セール'],
      text: ['🏷️ 割引（□%引き）は「もとの値段から □% 分を安くする」こと。',
        'コツ：残るのは (100 − □)％。',
        '例）1000円の 20%引き → 残り80% → 1000 × 0.8 = 800円',
        '（安くなる額は 1000 × 0.2 = 200円）'].join('\n')
    },
    {
      key: ['増し', 'まし', 'マージン', '利益', '増加', '値上げ', 'アップ'],
      text: ['📈 「□%増し」は「もとの量に □% 分を足す」こと。',
        'コツ：合計は (100 + □)％。',
        '例）500円の 30%増し → 130% → 500 × 1.3 = 650円',
        '（増える額は 500 × 0.3 = 150円）'].join('\n')
    },
    {
      key: ['消費税', '税', 'ぜい'],
      text: ['🧾 消費税は「○%増し」と同じ考え方だよ。',
        '例）税率10%なら、800円の品は 800 × 1.1 = 880円。',
        '増える税の額は 800 × 0.1 = 80円。'].join('\n')
    },
    {
      key: ['何%', 'なんパーセント', '比べ', 'くらべ'],
      text: ['🔍 「A は B の何％？」は、A ÷ B × 100 で出せるよ。',
        '例）20 は 80 の何％？ → 20 ÷ 80 = 0.25 → ×100 = 25％',
        '「くらべる数 ÷ もとの数 × 100」と覚えよう！'].join('\n')
    },
    {
      key: ['求め方', 'やり方', '計算のコツ', 'こつ', 'どうやって', '出し方', '解き方', 'もとめ方'],
      text: ['🧮 ％の計算は、まず ％ を小数に直すのが基本だよ。',
        '・「○の□%」→ ○ × (□÷100)　例）80の15% = 80 × 0.15 = 12',
        '・「□%引き」→ ○ × ((100−□)÷100)',
        '・「□%増し」→ ○ × ((100+□)÷100)',
        '・「AはBの何%」→ A ÷ B × 100',
        '数字を入れて聞いてくれたら、いっしょに解くよ！'].join('\n')
    },
    {
      key: ['こんにちは', 'はじめまして', 'やあ', 'おはよう', 'こんばんは', 'ヘルプ', 'help', '使い方', 'つかいかた'],
      text: ['👋 こんにちは！ぼくは「％の先生」だよ。',
        '・「80の15%は？」のように数字で聞くと、手順つきで答えるよ。',
        '・「割引って何？」「％の求め方」みたいな質問にも答えられる！',
        '下のボタンからも質問できるよ。'].join('\n')
    }
  ];

  function conceptAnswer(s) {
    for (var i = 0; i < CONCEPTS.length; i++) {
      if (has(s, CONCEPTS[i].key)) return { text: CONCEPTS[i].text };
    }
    return null;
  }

  function reply(input) {
    var s = normalize(input);
    if (!s) return { text: '数字を入れて「○の□%は？」のように聞いてみてね😊' };
    // まず計算を試す（数字＋％があるとき）
    var solved = trySolve(s);
    if (solved) return solved;
    // 概念質問
    var concept = conceptAnswer(s);
    if (concept) return concept;
    // 数字はあるが％が無い → ％のつけ方を案内
    if (/\d/.test(s)) {
      return { text: ['うーん、％計算の形にできなかったよ🤔',
        'たとえばこんな風に聞いてね：',
        '・「' + (s.match(/\d+/) || ['80'])[0] + ' の 20% は？」',
        '・「1200円の 15%引きは？」',
        '・「30 は 120 の何%？」'].join('\n') };
    }
    // それ以外
    return { text: ['ごめんね、その質問はまだ分からないかも🙏',
      'ぼくは「％の計算」が得意だよ。例えば：',
      '・「％ってなに？」',
      '・「割引のやり方」',
      '・「80の25%は？」'].join('\n') };
  }

  function greeting() {
    return ['👋 こんにちは！ぼくは「％の先生」。',
      '計算のしかたが分からなくても大丈夫。',
      '「80の15%は？」のように聞いたり、下のボタンを押してみてね。'].join('\n');
  }
  function suggestions() {
    return ['％ってなに？', '80の15%は？', '1200円の20%引きは？', '20は80の何%？', '割引のやり方', '消費税って？'];
  }

  global.Tutor = { reply: reply, greeting: greeting, suggestions: suggestions, normalize: normalize };
})(window);
